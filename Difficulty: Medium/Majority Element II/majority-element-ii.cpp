#define ll long long
class Solution {
  public:
    // Function to find the majority elements in the array
    vector<int> findMajority(vector<int>& arr) {
        // Your code goes here.
        vector<int> ans;
        map<ll, ll> mp;
        for(auto val : arr) mp[val]++;
        for(auto val : mp) {
            if(val.second > arr.size() / 3) {
                ans.push_back(val.first);
            }
        }
        return ans;
    }
};

